package day03_comments_escape_sequence;

public class TabAndNextLine {
    public static void main(String[] args) {
        // the folowing are about \t - tab
        System.out.println("This is not tabbed");
        System.out.println("\tThis is not tabbed"); // This is tabbed one time 4 space equal one tab
        System.out.println("\t\tThis is 2 times tabbed");


        System.out.println();
        // the followings are about new line - \n - new line
        System.out.println("1) Go to store");
        System.out.println("2) Grab milk");
        System.out.println("3) Pay cashier");


        System.out.println("\n1) Go to store\n2) Grab milk\n3) Pay cashier");

// to organize command + option + L
    }
}
