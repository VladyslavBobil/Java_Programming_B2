package day05_variebles;

public class BoleanExample {
    public static void main(String[] args) {
        boolean isSleepy = true,
                isHungry = false,
                isWeekend = false,
                isWeekday = true;
        System.out.println("isSleepy = " + isSleepy);
        System.out.println("isHungry = " + isHungry);
        System.out.println("It is a weekend " + isWeekend);
        System.out.println("It is a week day " + isWeekday);
    }
}
